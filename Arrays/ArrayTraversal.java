//Day-1 Array Traversal
// Time Complexity: O(n)
// space complexity: O(1)

class Main{
    public static void main(String[]args){
        int[] arr = {10,20,30,40,50};
        System.out.println("Array elements are:");
        
        for(int i = 0;i<arr.length;i++){
            System.out.print(arr[i]+" ");
        }
    }
}
